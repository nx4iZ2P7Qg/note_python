# DRY - don't repeat yourself
# 相同的东西不要写在多个地方，否则每次修改都需要同步
#
# KISS - keep it simple, stupid
# 避免不必要的复杂处理，使程序更健壮
#
# YAGNI - you aren't gonna need it
# 同样是避免复杂，不要考虑那些未来也不会碰到的问题

# 面向对象程序    SOLID
# Single responsibility principle
#     A class should have only a single responsibility, that is, only changes to one part of the software's
#     specification should be able to affect the specification of the class.
# Open–closed principle
#     "Software entities ... should be open for extension, but closed for modification."
# Liskov substitution principle
#     "Objects in a program should be replaceable with instances of their subtypes without altering the correctness of
#     that program." See also design by contract.
# Interface segregation principle
#     "Many client-specific interfaces are better than one general-purpose interface."
# Dependency inversion principle
#     One should "depend upon abstractions, [not] concretions."
